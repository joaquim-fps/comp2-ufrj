
public class Point {
	private double px, py;

	public Point(double px, double py) {
		this.px = px;
		this.py = py;
	}
	
	public double getPx() {
		return px;
	}

	public void setPx(double px) {
		this.px = px;
	}

	public double getPy() {
		return py;
	}

	public void setPy(double py) {
		this.py = py;
	}
}
